function initLikes() {
    const likeButtons = Array.from(document.querySelectorAll('[data-like-button]'));

    if (!likeButtons.length) {
        return;
    }

    const endpoint = 'https://tools.blackpiratex.com/api/likes';

    const getPayload = (button, action) => {
        const owner = button.getAttribute('data-like-owner');
        const url = button.getAttribute('data-like-url');
        return {
            action,
            owner_username: owner,
            post_url: url
        };
    };

    const getToastKey = (button) => {
        const owner = button.getAttribute('data-like-owner') || 'post';
        const url = button.getAttribute('data-like-url') || window.location.href;
        return `toast:${owner}:${url}`;
    };

    const getLabelText = (button, toasted) => {
        const defaultLabel = button.getAttribute('data-like-default-label') || 'Toast this post';
        const thanksLabel = button.getAttribute('data-like-thanks-label') || 'Thanks for toasting';
        return toasted ? thanksLabel : defaultLabel;
    };

    const readToastState = (button) => {
        try {
            return window.localStorage.getItem(getToastKey(button)) === '1';
        } catch (err) {
            return false;
        }
    };

    const writeToastState = (button, toasted) => {
        try {
            if (toasted) {
                window.localStorage.setItem(getToastKey(button), '1');
            } else {
                window.localStorage.removeItem(getToastKey(button));
            }
        } catch (err) {
            // Ignore storage failures and keep the UI state ephemeral.
        }
    };

    const setToastState = (button, toasted) => {
        const label = getLabelText(button, toasted);
        const labelEl = button.querySelector('[data-like-label]');

        button.classList.toggle('is-toasted', toasted);
        button.disabled = toasted || button.classList.contains('is-loading');
        button.setAttribute('aria-label', label);

        if (labelEl) {
            labelEl.textContent = label;
        }
    };

    const setLoading = (button, isLoading) => {
        button.classList.toggle('is-loading', isLoading);
        button.disabled = isLoading || button.classList.contains('is-toasted');
    };

    const request = async (button, action) => {
        const payload = getPayload(button, action);
        const res = await fetch(endpoint, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(payload)
        });

        if (!res.ok) {
            throw new Error('Request failed');
        }

        return res.json();
    };

    likeButtons.forEach((button) => {
        setToastState(button, readToastState(button));

        button.addEventListener('click', async () => {
            if (button.classList.contains('is-loading') || button.classList.contains('is-toasted')) {
                return;
            }

            setLoading(button, true);
            try {
                await request(button, 'like');
                writeToastState(button, true);
                setToastState(button, true);
            } catch (err) {
                // Leave the CTA unchanged if the toast request fails.
            } finally {
                setLoading(button, false);
            }
        });
    });
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLikes);
} else {
    initLikes();
}
