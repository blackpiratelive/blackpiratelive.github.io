function initWebringMembers() {
    const webringList = document.querySelector('[data-webring-list]');
    const webringStatus = document.querySelector('[data-webring-status]');

    if (!webringList || !webringStatus) {
        return;
    }

    fetch('https://webring.blackpiratex.com/api/latest')
        .then((response) => {
            if (!response.ok) {
                throw new Error('Unable to load webring members.');
            }
            return response.json();
        })
        .then((data) => {
            const sites = Array.isArray(data.sites) ? data.sites : [];

            if (sites.length === 0) {
                webringStatus.textContent = 'No members found right now.';
                return;
            }

            const fragment = document.createDocumentFragment();

            sites.forEach((site) => {
                if (!site || !site.url || !site.title) {
                    return;
                }

                const listItem = document.createElement('li');
                const link = document.createElement('a');
                const urlText = document.createElement('span');

                link.href = site.url;
                link.textContent = site.title;
                link.rel = 'noopener noreferrer';
                link.target = '_blank';

                urlText.className = 'home-webring-url';
                urlText.textContent = site.url;

                listItem.appendChild(link);
                listItem.appendChild(urlText);
                fragment.appendChild(listItem);
            });

            if (!fragment.childNodes.length) {
                webringStatus.textContent = 'No members found right now.';
                return;
            }

            webringList.innerHTML = '';
            webringList.appendChild(fragment);
            webringStatus.textContent = 'Latest verified members';
        })
        .catch(() => {
            webringStatus.textContent = 'Could not load webring members.';
        });
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initWebringMembers);
} else {
    initWebringMembers();
}

function initLikes() {
    const likeButtons = Array.from(document.querySelectorAll('[data-like-button]'));

    if (!likeButtons.length) {
        return;
    }

    const endpoint = 'https://tools.blackpiratex.com/api/likes';

    const getPayload = (button, action) => {
        const owner = button.getAttribute('data-like-owner');
        const url = button.getAttribute('data-like-url');
        return {
            action,
            owner_username: owner,
            post_url: url
        };
    };

    const updateCount = (button, count) => {
        const countEl = button.querySelector('[data-like-count]');
        if (!countEl) {
            return;
        }
        countEl.textContent = Number.isFinite(count) ? String(count) : '—';
    };

    const setLoading = (button, isLoading) => {
        if (isLoading) {
            button.classList.add('is-loading');
            button.disabled = true;
        } else {
            button.classList.remove('is-loading');
            button.disabled = false;
        }
    };

    const request = async (button, action) => {
        const payload = getPayload(button, action);
        const res = await fetch(endpoint, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(payload)
        });

        if (!res.ok) {
            throw new Error('Request failed');
        }

        return res.json();
    };

    likeButtons.forEach((button) => {
        setLoading(button, true);
        request(button, 'get')
            .then((data) => {
                const count = data && typeof data.likes === 'number' ? data.likes : undefined;
                updateCount(button, count);
            })
            .catch(() => {
                updateCount(button, undefined);
            })
            .finally(() => {
                setLoading(button, false);
            });

        button.addEventListener('click', async () => {
            if (button.classList.contains('is-loading')) {
                return;
            }
            setLoading(button, true);
            try {
                const data = await request(button, 'like');
                const count = data && typeof data.likes === 'number' ? data.likes : undefined;
                updateCount(button, count);
            } catch (err) {
                updateCount(button, undefined);
            } finally {
                setLoading(button, false);
            }
        });
    });
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLikes);
} else {
    initLikes();
}
